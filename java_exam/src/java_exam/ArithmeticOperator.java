package java_exam;

public class ArithmeticOperator {
	
	public static void main(String[] args) {
		int n = 1;
		System.out.println(n); // 1
		n++;
		System.out.println(n);
		System.out.println(n++);
		System.out.println(n);
		
		System.out.println(n);
		n--;
		System.out.println(n);
		System.out.println(n--);
		System.out.println(n);
		
		int m = 10;
		System.out.println(++m); // 11
		System.out.println(m++); // 11
		System.out.println(m); // 12
		
		
		System.out.println(--m); // 11
		System.out.println(m--); // 11
		System.out.println(m); // 10
	}

}
